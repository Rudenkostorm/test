str1=str(input("Введите строку: "))
l=len(str1)
print("Количество символов в строке:", l)
print("Весь список:", str1[::])
print("Нечетные элементы:", str1[::2])
print("Четные элементы:", str1[1::2])
print("Все элементы в обратном порядке:", str1[::-1])
print("Все элементы начиная с 6-го:", str1[5::])
print("Все элементы не доходя до 6-го:", str1[:5:])
print("Все элементы от предпоследнего до 3го в обратном порядке:", str1[-2:-l + 1:-1])